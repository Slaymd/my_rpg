#include "rpg.h"

void display_menu(scene_t *scene, window_t *window)
{
	sfRenderWindow_drawSprite(window->window, scene->sprite, NULL);
	for (int i = 0; scene->text[i]; i++) {
		sfRenderWindow_drawText(window->window,
		scene->text[i]->text, NULL);
	} for (int i = 0; scene->button[i]; i++) {
		sfRenderWindow_drawSprite(window->window,
		scene->button[i]->sprite, NULL);
	}
}
